# Write a program that asks for two numbers (either integer or float) and calculates their sum.

# Lab1: sum-calculator.py

# Asking for two numbers
number1 = float(input("Enter the first number: \t"))
number2 = float(input("Enter the second number: \t"))

# Calculating the sum
sum_result = number1 + number2

# Displaying the sum
print(f"The sum of {number1} and {number2} is: {sum_result}")
