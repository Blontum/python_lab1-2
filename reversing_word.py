# writing a program that will ask for a word and reverse that word
# cake4: reversing a word

# Get input from the user

word = input(" try to reverse  any word in python like, 'enter the word': ")

# Reverse the word using slicingi:
reversed_word = word[::-1]
# Display the results
print(f"my word is: {word}, and the reverse is: {reversed_word}")